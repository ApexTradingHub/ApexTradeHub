def get_market_regime():
    # Download 2y to ensure enough data for MA200
    spy = safe_download_single("SPY", "2y")
    qqq = safe_download_single("QQQ", "2y")

    if spy is None or qqq is None:
        return {"risk_on": False, "mode": "UNKNOWN",
                "summary": "Market filter unavailable -> defaulting to RISK-OFF"}

    def regime(df):
        # Squeeze handles MultiIndex columns from yfinance
        c = df["Close"].squeeze() if hasattr(df["Close"], "squeeze") else df["Close"]
        if isinstance(c, pd.DataFrame):
            c = c.iloc[:, 0]
        c = c.dropna()
        if len(c) < 50:
            return "UNKNOWN"

        ma20  = c.rolling(20).mean()
        ma50  = c.rolling(50).mean()
        ma200 = c.rolling(200).mean() if len(c) >= 200 else None

        lc  = safe_float(c.iloc[-1])
        l20 = safe_float(ma20.iloc[-1])
        l50 = safe_float(ma50.iloc[-1])

        # Only use MA200 if we have enough data and it's not NaN
        if ma200 is not None:
            l200_val = ma200.iloc[-1]
            l200 = safe_float(l200_val) if not pd.isna(l200_val) else None
        else:
            l200 = None

        if l200 is not None:
            if lc > l20 > l50 > l200:   return "STRONG"
            if lc > l50 and l50 > l200: return "OK"
        else:
            # No MA200 — use MA50 only
            if lc > l20 > l50: return "STRONG"
            if lc > l50:       return "OK"
        return "WEAK"

    sr, qr = regime(spy), regime(qqq)
    risk_on = sr in {"STRONG", "OK"} and qr in {"STRONG", "OK"}

    if sr == "STRONG" and qr == "STRONG": mode = "BULLISH"
    elif risk_on:                          mode = "MIXED"
    else:                                  mode = "BEARISH"

    # SPY 20-day performance for relative strength calculation
    spy_close = spy["Close"].squeeze().dropna()
    spy_perf_20 = 0.0
    if len(spy_close) >= 21:
        spy_perf_20 = float((spy_close.iloc[-1] / spy_close.iloc[-21] - 1) * 100)

    # Sector ETF momentum (20d performance)
    sector_etfs = {
        "Energy":            "XLE",
        "Technology":        "XLK",
        "Healthcare":        "XLV",
        "Financials":        "XLF",
        "Industrials":       "XLI",
        "Consumer Defensive":"XLP",
        "Consumer Cyclical": "XLY",
        "Basic Materials":   "XLB",
        "Utilities":         "XLU",
        "Real Estate":       "XLRE",
        "Communication":     "XLC",
    }
    sector_momentum = {}
    try:
        etf_symbols = list(sector_etfs.values())
        etf_batch = yf.download(" ".join(etf_symbols), period="3mo",
                                group_by="ticker", auto_adjust=True,
                                progress=False, threads=True)
        for sector, etf in sector_etfs.items():
            try:
                if len(etf_symbols) == 1:
                    c = etf_batch["Close"].squeeze().dropna()
                else:
                    c = etf_batch[etf]["Close"].squeeze().dropna()
                if len(c) >= 21:
                    sector_momentum[sector] = round(float((c.iloc[-1] / c.iloc[-21] - 1) * 100), 2)
                else:
                    sector_momentum[sector] = 0.0
            except Exception:
                sector_momentum[sector] = 0.0
    except Exception:
        for sector in sector_etfs:
            sector_momentum[sector] = 0.0

    return {"risk_on": risk_on, "mode": mode,
            "summary": f"SPY={sr} | QQQ={qr} | Market={mode}",
            "spy_perf_20": spy_perf_20,
            "sector_momentum": sector_momentum}



