def load_signal_memory():
    """Returns {ticker: 'YYYY-MM-DD'} of signals from last 7 days."""
    from datetime import datetime, timedelta
    try:
        with open(SIGNAL_MEMORY, "r", encoding="utf-8") as f:
            data = json.load(f)
        # Prune entries older than 7 days
        cutoff = (datetime.now() - timedelta(days=7)).strftime("%Y-%m-%d")
        return {k: v for k, v in data.items() if v >= cutoff}
    except Exception:
        return {}


def save_signal_memory(mem):
    try:
        with open(SIGNAL_MEMORY, "w", encoding="utf-8") as f:
            json.dump(mem, f, indent=2, ensure_ascii=False)
    except Exception:
        pass


def update_signal_memory(tickers, today):
    """Add today's tickers to memory and persist."""
    mem = load_signal_memory()
    for t in tickers:
        mem[t] = today
    save_signal_memory(mem)


def signal_in_window(ticker, today, days=DUPLICATE_WINDOW_DAYS):
    """True if ticker was signaled within the last `days` days."""
    from datetime import datetime
    mem = load_signal_memory()
    if ticker not in mem:
        return False
    try:
        last = datetime.strptime(mem[ticker], "%Y-%m-%d")
        today_dt = datetime.strptime(today, "%Y-%m-%d")
        return abs((today_dt - last).days) <= days
    except Exception:
        return False


