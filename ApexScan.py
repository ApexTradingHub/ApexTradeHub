def save_signals(candidates):
    """Appends new signals to apex_signals.json (no duplicates)."""
    from datetime import datetime
    today = datetime.now().strftime("%Y-%m-%d")
    signals_file = "apex_signals.json"
    try:
        with open(signals_file, "r", encoding="utf-8") as f:
            existing = json.load(f)
    except FileNotFoundError:
        existing = []

    # Block same ticker if seen in last DUPLICATE_WINDOW_DAYS days
    today_dt = datetime.strptime(today, "%Y-%m-%d")
    recent_tickers = {
        r["ticker"] for r in existing
        if abs((today_dt - datetime.strptime(r["date"], "%Y-%m-%d")).days) <= DUPLICATE_WINDOW_DAYS
    }

    existing_keys = {(r["date"], r["ticker"]) for r in existing}
    new_entries = []
    skipped_recent = []
    for c in candidates:
        if (today, c["ticker"]) in existing_keys:
            continue
        if c["ticker"] in recent_tickers:
            skipped_recent.append(c["ticker"])
            continue
        entry = dict(c)
        entry["date"] = today
        new_entries.append(entry)

    if skipped_recent:
        print(f"  Skipped (seen in last 5 days): {', '.join(skipped_recent)}")

    existing.extend(new_entries)
    with open(signals_file, "w", encoding="utf-8") as f:
        json.dump(existing, f, indent=2, ensure_ascii=False)
    print(f"Signals saved: {len(new_entries)} new -> {signals_file}  (total: {len(existing)})")


