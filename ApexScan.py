# REVERSAL DETECTION (Phase F.1: 4 evidence-based factors → composite RSS)
# (Kept for compat — REVERSAL setup itself disabled in scan_ticker)
# =============================================================
def compute_reversal_indicators(df, rsi_series):
    """
    Returns dict of bullish-reversal signals:
      - bullish_divergence: price makes lower low, RSI makes higher low (Wilder)
      - hammer_candle: long lower shadow, body near top — selling rejected
      - bullish_engulfing: today's green body engulfs yesterday's red body
      - higher_low_structure: recent low > older low in last 30 days
      - obv_divergence: price lower low, OBV higher low (institutional accumulation)
      - selling_climax: recent big down-day with vol_ratio >= 2.5 (capitulation)
    """
    out = {
        "bullish_divergence":   False,
        "hammer_candle":        False,
        "bullish_engulfing":    False,
        "higher_low_structure": False,
        "obv_divergence":       False,
        "selling_climax":       False,
    }
    if df is None or len(df) < 30:
        return out

    closes = df["Close"].values
    highs  = df["High"].values
    lows   = df["Low"].values
    opens  = df["Open"].values
    vols   = df["Volume"].values
    n = len(closes)

    # --- 1. Bullish RSI-Divergence (Wilder classic) ---
    # Find lowest close in days [-30, -10] (old low) and [-10, -1] (new low)
    if rsi_series is not None and len(rsi_series) >= 30:
        rsi_vals = rsi_series.values
        old_win = closes[-30:-10]
        new_win = closes[-10:]
        old_low_idx = old_win.argmin() + (n - 30)
        new_low_idx = new_win.argmin() + (n - 10)
        if closes[new_low_idx] < closes[old_low_idx]:
            # Price made lower low → check if RSI made higher low
            if not pd.isna(rsi_vals[new_low_idx]) and not pd.isna(rsi_vals[old_low_idx]):
                if rsi_vals[new_low_idx] > rsi_vals[old_low_idx]:
                    out["bullish_divergence"] = True

    # --- 2. Hammer Candle ---
    # Body in upper 50%, lower shadow >= 2× body, decent range
    body = abs(closes[-1] - opens[-1])
    upper_shadow = highs[-1] - max(closes[-1], opens[-1])
    lower_shadow = min(closes[-1], opens[-1]) - lows[-1]
    total_range = highs[-1] - lows[-1]
    if total_range > 0 and body > 0:
        # Hammer needs significant lower shadow and small body up top
        if lower_shadow >= 2 * body and upper_shadow <= body * 0.5 and total_range / closes[-1] > 0.015:
            out["hammer_candle"] = True

    # --- 3. Bullish Engulfing ---
    # Yesterday red, today green AND today's body covers yesterday's body
    if n >= 2:
        prev_red   = closes[-2] < opens[-2]
        today_green = closes[-1] > opens[-1]
        if prev_red and today_green:
            if (closes[-1] >= opens[-2]) and (opens[-1] <= closes[-2]):
                out["bullish_engulfing"] = True

    # --- 4. Higher-Low Structure ---
    # Lowest low in [-30, -15] vs lowest low in [-15, -1]
    if n >= 30:
        old_segment = lows[-30:-15]
        new_segment = lows[-15:]
        if new_segment.min() > old_segment.min():
            # Plus: confirm we're not just sideways — old segment had a real low
            range_old = old_segment.max() - old_segment.min()
            avg_close = closes[-30:-15].mean()
            if range_old / avg_close > 0.03:  # at least 3% range
                out["higher_low_structure"] = True

    # --- 5. OBV-Divergence (On-Balance-Volume) ---
    # OBV cumulative: vol on up days adds, on down days subtracts
    if n >= 30:
        obv_changes = []
        for i in range(1, n):
            if closes[i] > closes[i-1]: obv_changes.append(vols[i])
            elif closes[i] < closes[i-1]: obv_changes.append(-vols[i])
            else: obv_changes.append(0)
        import numpy as np
        obv = np.cumsum([0] + obv_changes)
        # Check divergence on same lows as price
        old_close_low_idx = closes[-30:-10].argmin() + (n - 30)
        new_close_low_idx = closes[-10:].argmin() + (n - 10)
        if closes[new_close_low_idx] < closes[old_close_low_idx]:
            if obv[new_close_low_idx] > obv[old_close_low_idx]:
                out["obv_divergence"] = True

    # --- 6. Selling Climax: big down-day with extreme volume in last 5 days ---
    if n >= 21:
        vol20_avg = vols[-20:].mean()
        if vol20_avg > 0:
            for i in range(-5, 0):
                if closes[i] < closes[i-1]:  # down day
                    if vols[i] / vol20_avg >= 2.5:
                        out["selling_climax"] = True
                        break

    return out


def compute_reversal_strength_score(rev_signals):
    """Composite RSS — weights per indicator strength (higher = stronger evidence)."""
    rss = 0
    if rev_signals.get("bullish_divergence"):    rss += 25  # strongest
    if rev_signals.get("obv_divergence"):        rss += 20
    if rev_signals.get("higher_low_structure"):  rss += 15
    if rev_signals.get("bullish_engulfing"):     rss += 15
    if rev_signals.get("hammer_candle"):         rss += 12
    if rev_signals.get("selling_climax"):        rss += 13
    return rss


