def find_target(df, buy_above, atr14, setup, risk_pct):
    """
    Finds a realistic price target using multiple methods:
    1. Previous swing highs (historical resistance)
    2. Fibonacci 1.272 / 1.618 extensions from last swing
    3. Round price levels (psychological resistance)
    4. ATH + buffer if near all-time high
    5. Fallback: RR-based with variable multiplier per setup

    Picks the nearest valid candidate above buy_above (min 4%, max 25% upside).
    """
    MIN_UPSIDE = 4.0    # minimum target distance %
    MAX_UPSIDE = 25.0   # maximum before considered unrealistic
    RR_FALLBACK = {"BREAKOUT": 2.0, "PRE-ROCKET": 2.5, "POSITION": 3.0}

    highs  = df["High"].values
    lows   = df["Low"].values
    n      = len(highs)

    candidates = []

    # --- Method 1: Swing highs (last 252 bars) ---
    SWING_W = 3
    lookback  = min(252, n - 1)
    start_idx = max(SWING_W, n - lookback)
    for i in range(start_idx, n - SWING_W):
        h = highs[i]
        if h <= buy_above:
            continue
        left  = highs[max(0, i - SWING_W) : i]
        right = highs[i + 1 : i + SWING_W + 1]
        if len(left) and len(right) and h > max(left) and h > max(right):
            candidates.append(round(h, 2))

    # --- Method 2: Fibonacci extensions ---
    # Find last significant swing low and swing high before buy_above
    recent = min(60, n - 1)
    swing_low  = float(min(lows[-recent:]))
    swing_high = float(max(highs[-recent:]))

    if swing_high > swing_low:
        move = swing_high - swing_low
        fib_1272 = round(swing_low + move * 1.272, 2)
        fib_1618 = round(swing_low + move * 1.618, 2)
        if fib_1272 > buy_above:
            candidates.append(fib_1272)
        if fib_1618 > buy_above:
            candidates.append(fib_1618)

    # --- Method 3: Round price levels ---
    # Find the next round numbers above buy_above
    def next_round_levels(price, above):
        levels = []
        # Determine magnitude of rounding
        if above > 500:   steps = [10, 25, 50, 100]
        elif above > 100: steps = [5, 10, 25, 50]
        elif above > 50:  steps = [2.5, 5, 10, 25]
        elif above > 10:  steps = [1, 2.5, 5, 10]
        else:             steps = [0.5, 1, 2.5, 5]
        for step in steps:
            rounded = math.ceil(above / step) * step
            # Add a few levels
            for i in range(5):
                lvl = round(rounded + i * step, 2)
                if lvl > above:
                    levels.append(lvl)
        return levels

    round_levels = next_round_levels(buy_above, buy_above)
    candidates.extend(round_levels)

    # --- Method 4: ATH + small buffer if near all-time high ---
    ath = float(df["High"].max())
    if ath > buy_above:
        candidates.append(round(ath, 2))
        candidates.append(round(ath * 1.05, 2))
        candidates.append(round(ath * 1.10, 2))

    # --- Step 5: Enforce minimum target = 2x ATR from entry ---
    # This ensures targets are not trivially close (avoids <5% upside)
    min_target_mult = {"BREAKOUT": 2.5, "PRE-ROCKET": 3.0, "POSITION": 4.0}
    atr_min_target  = buy_above + (atr14 * min_target_mult.get(setup, 2.5))
    atr_min_upside  = (atr_min_target / buy_above - 1) * 100

    # Use ATR minimum as floor for MIN_UPSIDE
    effective_min = max(MIN_UPSIDE, atr_min_upside)

    candidates = sorted(set(candidates))
    target = None
    for c in candidates:
        upside = (c / buy_above - 1) * 100
        if effective_min <= upside <= MAX_UPSIDE:
            target = c
            break

    # --- Fallback: ATR-based minimum target ---
    if target is None:
        rr_mult = RR_FALLBACK.get(setup, 2.0)
        rr_target = round(buy_above * (1 + (risk_pct * rr_mult) / 100), 2)
        # Take the larger of RR-based and ATR-based target
        target = max(rr_target, round(atr_min_target, 2))

    return target


