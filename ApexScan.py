            risk_pct  = (buy_above - stop) / buy_above * 100 if buy_above > 0 else 99
            if risk_pct < 3.0:
                stop = buy_above * 0.96; risk_pct = 4.0
            elif risk_pct > 8.0:
                debug["risk_too_high"] += 1; return None
            # Target: extension toward 52w high or +15%
