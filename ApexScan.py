def run_scan(tickers, market_regime):
    """
    Three-pass adaptive scan:
      Pass 0 – strict filters
      Pass 1 – relaxed filters   (if pass 0 < MIN_SIGNALS)
      Pass 2 – broad sweep       (if pass 1 < MIN_SIGNALS)
    Returns list of candidates (best pass used).
    """
    all_frames = {}
    debug = Counter()

    batches = list(chunked(tickers, BATCH_SIZE))
    for batch in tqdm(batches, desc="Downloading"):
        raw = None
        try:
            with suppress_output():
                raw = yf.download(
                    tickers=" ".join(batch),
                    period=HISTORY_PERIOD,
                    auto_adjust=True,
                    progress=False,
                    threads=True,
                    group_by="ticker",
                )
        except Exception:
            pass

        for ticker in batch:
            frame = extract_ticker_frame(raw, ticker)
            if frame is not None:
                all_frames[ticker] = frame

    print(f"  Usable frames: {len(all_frames)} / {len(tickers)}")

    sector_cache = load_sector_cache()

    for relax in range(3):
        candidates = []
        pass_debug = Counter()
        for ticker, frame in all_frames.items():
            result = scan_ticker(ticker, frame, market_regime, pass_debug, sector_cache, relax=relax)
            if result is not None:
                candidates.append(result)

        debug += pass_debug

        if len(candidates) >= MIN_SIGNALS:
            if relax > 0:
                print(f"  Note: filters relaxed to level {relax} to find signals.")
            break
        else:
            print(f"  Pass {relax}: {len(candidates)} signals – relaxing filters...")

    # Deduplicate tickers
    seen, unique = set(), []
    for c in sorted(candidates, key=lambda x: x["score"], reverse=True):
        if c["ticker"] not in seen:
            seen.add(c["ticker"])
            unique.append(c)

    return unique[:MAX_SIGNALS], debug


