            if risk_pct < 3.0:
                stop = buy_above * 0.92; risk_pct = 8.0  # wider stop OK for Stage 2
            elif risk_pct > 15.0:
                debug["risk_too_high"] += 1; return None
            # Target: measured move (base_high + base_width)
            base_width = stage2_data["base_high"] - stage2_data["base_low"]
            target = round(stage2_data["base_high"] + base_width * 1.5, 2)
