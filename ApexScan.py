def clean_universe(tickers):
    cleaned = []
    for t in tickers:
        if not t or len(t) > 8:
            continue
        if "^" in t or "/" in t or "=" in t:
            continue
        if t in DEAD_TICKERS or t in BAD_PERFORMERS:
            continue
        cleaned.append(t)
    return unique_keep_order(cleaned)


