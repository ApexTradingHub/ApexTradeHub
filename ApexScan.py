            setup     = "SHORT_SQUEEZE"
            horizon   = "2-4 weeks"
            buy_above = round(squeeze_data["breakout_level"], 2)
            action    = f"BUY above {buy_above:.2f} (squeeze, {squeeze_data['short_pct']:.0f}% short)"
