        if stage2_data:
            chosen_setup = "STAGE_2"
        elif vcp_data:
            chosen_setup = "VCP"
        elif squeeze_data:
            chosen_setup = "SHORT_SQUEEZE"
        elif breakout_setup:
            chosen_setup = "BREAKOUT"
        else:
            debug["fail_setup"] += 1
            return None

        # Market filter: RISK-OFF → only BREAKOUT-style allowed (STAGE_2/VCP need trend)
        if not market_regime["risk_on"] and relax == 0:
            # All Phase G setups need trend — RISK-OFF would block all anyway via trend_ok
            pass

