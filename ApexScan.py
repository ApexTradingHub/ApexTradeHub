# CATALYSTS (technical, computed from OHLCV — no API needed)
# =============================================================
def compute_technical_catalysts(df, atr14_now, vol_ratio):
    """
    Detects 4 evidence-based price-jump triggers from chart data:
      - Pocket Pivot (Morales/IBD): up-day where today's vol > max down-vol last 10d
      - Volume Climax: vol_ratio >= 3.0 today
      - Up-Gap: open today >2% above prev close
      - VCP (Minervini): ATR contracting >=30% over last 4 weeks
    Returns dict of signals + magnitudes.
    """
    out = {
        "pocket_pivot_recent": False,  # in last 5 trading days
        "volume_climax": False,
        "up_gap_pct": 0.0,
        "gap_signal": False,
        "vcp_strength": 0.0,    # 0..1
        "vcp_signal": False,
    }
    if df is None or len(df) < 21:
        return out

    closes = df["Close"].values
    vols   = df["Volume"].values
    opens  = df["Open"].values
    n = len(closes)

    # --- Pocket Pivot in last 5 days ---
    for i in range(1, 6):
        idx = n - i
        if idx < 11:
            break
        if closes[idx] <= closes[idx-1]:
            continue  # need up-day
        sub_close      = closes[idx-10:idx]
        sub_vol        = vols[idx-10:idx]
        sub_close_prev = closes[idx-11:idx-1]
        down_mask = sub_close < sub_close_prev
        if down_mask.any():
            max_down_vol = sub_vol[down_mask].max()
            if vols[idx] > max_down_vol:
                out["pocket_pivot_recent"] = True
                break

    # --- Volume Climax (today) ---
    out["volume_climax"] = vol_ratio >= 3.0

    # --- Up-Gap (today) ---
    if n >= 2 and closes[-2] > 0:
        out["up_gap_pct"] = (opens[-1] - closes[-2]) / closes[-2] * 100
        out["gap_signal"] = out["up_gap_pct"] > 2.0

    # --- VCP: ATR contraction over 4 weeks ---
    if "ATR14" in df.columns and n >= 21:
        atr_old = df["ATR14"].iloc[-21]
        if not pd.isna(atr_old) and atr_old > 0 and atr14_now > 0:
            contraction = 1 - (atr14_now / atr_old)
            out["vcp_strength"] = max(0.0, min(contraction, 1.0))
            out["vcp_signal"] = out["vcp_strength"] >= 0.30

    return out


