def safe_download_single(ticker, period="1y"):
    try:
        with suppress_output():
            df = yf.download(ticker, period=period, auto_adjust=True,
                             progress=False, threads=False)
        return df.copy() if df is not None and not df.empty else None
    except Exception:
        return None


def extract_ticker_frame(raw_data, ticker):
    try:
        if raw_data is None or raw_data.empty:
            return None
        if isinstance(raw_data.columns, pd.MultiIndex):
            lvl0 = raw_data.columns.get_level_values(0).tolist()
            lvl1 = raw_data.columns.get_level_values(1).tolist()
            if ticker in lvl0:
                df = raw_data[ticker].copy()
            elif ticker in lvl1:
                df = raw_data.xs(ticker, axis=1, level=1).copy()
            else:
                return None
        else:
            df = raw_data.copy()

        required = ["Open", "High", "Low", "Close", "Volume"]
        if any(c not in df.columns for c in required):
            return None
        df = df[required].dropna().copy()
        return df if len(df) >= 30 else None
    except Exception:
        return None


