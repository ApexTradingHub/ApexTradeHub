def get_market_regime():
    # Download 2y to ensure enough data for MA200
    spy = safe_download_single("SPY", "2y")
    qqq = safe_download_single("QQQ", "2y")

    if spy is None or qqq is None:
        return {"risk_on": False, "mode": "UNKNOWN",
                "summary": "Market filter unavailable -> defaulting to RISK-OFF"}

    def regime(df):
        # Squeeze handles MultiIndex columns from yfinance
        c = df["Close"].squeeze() if hasattr(df["Close"], "squeeze") else df["Close"]
        if isinstance(c, pd.DataFrame):
            c = c.iloc[:, 0]
        c = c.dropna()
        if len(c) < 50:
            return "UNKNOWN"

        ma20  = c.rolling(20).mean()
        ma50  = c.rolling(50).mean()
        ma200 = c.rolling(200).mean() if len(c) >= 200 else None

        lc  = safe_float(c.iloc[-1])
