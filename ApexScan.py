def detect_vcp_setup(df, atr14_now, vol_ratio, ma50, ma150, close, high, atr_pct):
    """
    Minervini VCP (Volatility Contraction Pattern):
    Stock in Stage 2 + volatility shrinking 30%+ over 4-6 weeks + tight base + breakout.
    Returns dict with breakout_level/base_low/contraction% if detected, else None.
    """
    n = len(df)
    if n < 100: return None
    # 1. Stage 2 confirmation
    if not (close > ma150 > 0 and ma50 > ma150):
        return None
    # 2. ATR contraction 30%+ over 25 bars
    if "ATR14" not in df.columns: return None
    atr_old = df["ATR14"].iloc[-25]
    if pd.isna(atr_old) or atr_old <= 0: return None
    contraction = 1 - (atr14_now / atr_old)
    # 2026-05-28: loosened 0.30 -> 0.20. Backtest (2yr, VCP-isolated): 0.30 fired ~0 signals;
    # 0.20 gave n=9, WR 88.9%, PF 7.16. Still rare (~4-5/yr) but high quality. Other VCP
    # filters (20d-range <12%, vol>=1.2, atr_pct<=6%) keep it precise.
    if contraction < 0.20: return None
    # 3. Tight base: 20d range < 12%
    last20_high = df["High"].iloc[-21:-1].max()
    last20_low  = df["Low"].iloc[-21:-1].min()
    if last20_low <= 0: return None
    base_range_pct = (last20_high / last20_low - 1) * 100
    if base_range_pct > 12: return None
    # 4. Breakout: today's high touches above last 10d-high
    last10_high = df["High"].iloc[-11:-1].max()
    breakout_level = last10_high * 1.002
    if high < breakout_level * 0.998: return None
    # 5. Volume confirmation
    if vol_ratio < 1.2: return None
    # 6. Reasonable volatility (not extreme)
    if atr_pct > 6.0: return None
    return {
        "breakout_level":  breakout_level,
        "base_low":        last20_low,
        "contraction_pct": round(contraction * 100, 1),
        "base_range_pct":  round(base_range_pct, 1),
    }


def detect_short_squeeze_setup(df, short_pct, ma20, ma50, close, vol_ratio, rsi14):
    """
    Short Squeeze: high short interest + bullish technicals + accelerating move.
    Returns dict if detected, else None.
    """
    if short_pct is None or short_pct < 15.0: return None
    if not (close > ma20 and ma20 > ma50): return None  # uptrend
    n = len(df)
    if n < 6: return None
    closes = df["Close"].values
    perf_5d = (closes[-1] / closes[-6] - 1) * 100
    if perf_5d < 3.0: return None   # squeeze must be active
    if vol_ratio < 1.3: return None  # volume confirms
    if not (50 <= rsi14 <= 78): return None  # momentum but not extreme overbought
    return {
        "short_pct":       round(short_pct, 1),
        "perf_5d":         round(perf_5d, 1),
        "breakout_level":  df["High"].iloc[-11:-1].max() * 1.003,
    }


def detect_stage2_transition(df, ma150_series, close):
    """
    Weinstein Stage 2: long flat base (Stage 1) breaking into uptrend (Stage 2).
    Rare but powerful — flat MA150 turning up + breakout above base high.
    """
    n = len(df)
    if n < 252: return None   # need full year history
    # 1. Long flat base: 180d high/low ratio < 1.4 (lateral range)
    last180_close = df["Close"].iloc[-180:]
    base_high = float(last180_close.max())
    base_low  = float(last180_close.min())
    if base_low <= 0 or (base_high / base_low) > 1.4: return None
    # 2. MA150 was flat 5→1 months ago, now rising
    if ma150_series is None: return None
    try:
        ma150_now    = safe_float(ma150_series.iloc[-1])
        ma150_1m_ago = safe_float(ma150_series.iloc[-21])
        ma150_5m_ago = safe_float(ma150_series.iloc[-100])
    except Exception:
        return None
    if not (ma150_now and ma150_1m_ago and ma150_5m_ago): return None
    flat_change = abs(ma150_1m_ago / ma150_5m_ago - 1)
    if flat_change > 0.05: return None  # was flat (<5% change over 4 months)
    rise_change = ma150_now / ma150_1m_ago - 1
    if rise_change < 0.01: return None  # now rising
    # 3. Breakout above base high
    if close < base_high * 0.99: return None
    return {
        "base_high":       round(base_high, 2),
        "base_low":        round(base_low, 2),
        "ma150_rise_pct":  round(rise_change * 100, 2),
        "base_width_pct":  round((base_high / base_low - 1) * 100, 1),
    }


