def main():
    # Sync with GitHub before scan to avoid duplicate signals
    import subprocess, os
    if os.path.exists(".git"):
        try:
            subprocess.run(["git", "pull", "origin", "master"],
                           capture_output=True, check=True)
            print("Git: Synced with GitHub.")
        except subprocess.CalledProcessError:
            print("Git: Pull failed or no remote – continuing anyway.")

    # Load tickers from both markets
    us_tickers = clean_universe(load_tickers(US_TICKER_FILE))
    eu_tickers = clean_universe(load_tickers(EU_TICKER_FILE))
    all_tickers = unique_keep_order(us_tickers + eu_tickers)

    if not all_tickers:
        print("No tickers loaded. Check us_tickers.txt and eu_tickers.txt")
        return

    print(f"Tickers loaded: {len(us_tickers)} US  +  {len(eu_tickers)} EU  =  {len(all_tickers)} total")

    market_regime = get_market_regime()
    save_market_regime(market_regime)
    print(f"Market regime : {market_regime['summary']}\n")

    candidates, debug = run_scan(all_tickers, market_regime)

    print(f"\nCandidates before sector enrichment: {len(candidates)}")
    print("Debug summary:")
    for k, v in debug.most_common(12):
        print(f"  {k}: {v}")

    candidates = enrich_sector(candidates)
    candidates = apply_sector_cap(candidates)
    print_signals(candidates)

    # Filter out tickers seen in last DUPLICATE_WINDOW_DAYS days (before Telegram AND before save)
    from datetime import datetime as _dt
    today = _dt.now().strftime("%Y-%m-%d")
    today_dt = _dt.strptime(today, "%Y-%m-%d")
    try:
        with open("apex_signals.json", "r", encoding="utf-8") as f:
            _existing = json.load(f)
    except FileNotFoundError:
        _existing = []
    recent_tickers = {
        r["ticker"] for r in _existing
        if abs((today_dt - _dt.strptime(r["date"], "%Y-%m-%d")).days) <= DUPLICATE_WINDOW_DAYS
        and r.get("relax_level", 0) == 0
    }
    deduped = [c for c in candidates if c["ticker"] not in recent_tickers]
    skipped = [c["ticker"] for c in candidates if c["ticker"] in recent_tickers]
    if skipped:
        print(f"  Duplicate filter: {', '.join(skipped)} skipped (seen in last {DUPLICATE_WINDOW_DAYS} days).")
    candidates = deduped

    save_signals(candidates)

    # Send Telegram notification — eligibility → quality → top 2 by score
    if candidates:
        # Telegram eligibility:
        #   BREAKOUT: only from strict pass (relax=0) — relax=1 BREAKOUTs underperform
        #   REVERSAL: always eligible (is always relax=0 by construction)
        def tg_eligible(c):
            if c.get("setup") == "BREAKOUT":
                return c.get("relax_level", 0) == 0
            return True

        eligible = [c for c in candidates if tg_eligible(c)]
        n_filtered = len(candidates) - len(eligible)
        if n_filtered:
            print(f"  Telegram filter: {n_filtered} weak BREAKOUT(s) from relax=1 excluded.")

        # Apply quality filter on eligible pool (setup-specific score thresholds)
        def _min_score_for(c):
            return TG_MIN_SCORE.get(c.get("setup", ""), TG_MIN_SCORE_DEFAULT)

        quality = [c for c in eligible if
                   c.get("rr", 0)         >= TG_MIN_RR and
                   c.get("upside_pct", 0) >= TG_MIN_UPSIDE and
                   c.get("score", 0)      >= _min_score_for(c)]

        if not quality and eligible:
            # Fallback: best eligible even if below quality threshold
            quality = sorted(eligible, key=lambda x: x.get("score", 0), reverse=True)[:1]
            print("  Note: No signals passed quality filter — sending best eligible.")

        if quality:
            top2 = sorted(quality, key=lambda x: x.get("score", 0), reverse=True)[:2]
            msg = build_telegram_message(top2, market_regime)
            send_telegram(msg)
        else:
            # Alle Kandidaten rausgefiltert (z.B. nur relax=1 BREAKOUTs, keine REVERSALs)
            print("  Note: All candidates filtered out — sending no-signal message.")
            send_telegram(get_no_signal_message(market_regime))
    else:
        send_telegram(get_no_signal_message(market_regime))


if __name__ == "__main__":
    main()