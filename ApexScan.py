            min_dist_pct   = max(3.0, atr_pct_val * 1.5)
            multiplier     = 1.5
            candidate_dist = ((buy_above - stop_candidate) / buy_above * 100
                              if buy_above > 0 else 99)

            if candidate_dist < min_dist_pct:
                stop = buy_above * (1 - min_dist_pct / 100)
            elif candidate_dist > 6:
                stop = buy_above - (atr14 * multiplier)
            else:
                stop = stop_candidate

